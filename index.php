       Cette année, la compétition d'art du "Colorado State Fair" à donné des prix dans toutes les cathégories habituelles : Peinture , Crochet, Sculpture ...<br><br>Mais un participant, Jason M.Allen, n'as pas créé son oeuvre a partir de peinture ou d'argile.Il l'a créer avec Midjourney, un programme d'intélligence artificiel qui transforme des lignes de texte en image hyper réalistique.<br><br>L'oeuvre de Mr.Allen, "Théâtre D'opéra Spatial" à remporté le prix pour le concours d'artiste digital emergant, en feaisant la premiere oeuvre d'art généré avec IA à remporter un titre, et démarant une polémique de la part des artistes l'accusant de tricherie.<br><br>
       Mr.Allen Défend son oeuvre, clarifiant qu'elle à été entré sous le nom de "Jason M.Allen via Midjourney", a été crée a l'aide d'une IA, et qu'il n'a mentit à personne.<br><br>
       "Je ne vais pas m'excuser" dit-il. "j'ai gagné et je n'ai brisé aucunes regles"<br><br>
       La génération d'art via des IA existe depuis des années, mais les outils Sortis cette années comme "DALL-E 2", "Midjourney" et "Stable Diffusion" ont rendu possible pour des amateurs de creer des oeuvres complexe, abstraites ou photorealistiques simplement avec quelques lignes de texte.<br><br>
       Ces appliquations rendent certain artistes nerveux pour leur avenir, pourquoi payer pour de l'art quand ils pourrais le genereux eux meme ?<br><br>
       Ils ont aussi soulever de gros débats sur l'aspect éthique de l'art généré par IA, et certain les accuse d'etre une forme high tech de plagia.
